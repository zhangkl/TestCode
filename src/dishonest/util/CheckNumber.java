    public static void showSample(BufferedImage image) {
        for (int i = 0; i < 4; i++) {
            StringBuffer stringBuffer = new StringBuffer();
            for (int y = 0; y < height; y++) {
                for (int x = 0; x < width; x++) {
                    stringBuffer.append(checkRGB(image.getRGB(offx + offw * i + x, offy + offh * i + y)));
                    stringBuffer.append(",");
                }
            }
            System.out.println(stringBuffer.toString());
        }
    }

    public static String getCheckNumber(BufferedImage image) {
        String s = "";
        for (int i = 0; i < 4; i++) {
            int value = compareSample(image, offx + offw * i, offy + offh * i);
            s = s + value;
        }
        return s;
    }

    private static int compareSample(BufferedImage image, int offx, int offy) {
        int arrIndex[] = {
                8, 9, 6, 3, 0, 5, 2, 4, 7, 1
        };
        for (int i = 0; i < arrIndex.length; i++) {
            int value = compareSample(image, offx, offy, arrSample[arrIndex[i]]);
            if (value > 111) {
                return arrIndex[i];
            }
        }
        return 8;
    }

    private static int compareSample(BufferedImage image, int offx, int offy, int[] arrSample) {
        int value = 0;
        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                int rgb = checkRGB(image.getRGB(offx + x, offy + y));
                if (rgb == arrSample[y * width + x]) {
                    value++;
                }
            }
        }
        return value;
    }

    private static int checkRGB(int rgb) {
        int r = (rgb >> 16) & 0xff;
        int g = (rgb >> 8) & 0xff;
        int b = rgb & 0xff;
        if (r + g + b > 432) {
            return 0;
        } else {
            return 1;
        }
    }
